for i in range(1,N):
    a0 = i % 10
    a1 = ((i - a0) // 10) % 10
    a2 = ((i - (a1*10+a0)) // 100) % 10
    a3 = ((i - (a2*100 + a1*10 + a0)) // 1000) % 10
    a4 = ((i - (a3*1000 + a2*100 + a1*10 + a0)) // 10000) % 10
    a5 = ((i - (a4*10000 + a3*1000 + a2*100 + a1*10 + a0)) //100000) % 10
    if arr[i] == 0:
        arr[i] = a5 + a4 + a3 + a2 + a1 + a0 + i
        if arr[i] == N:
            print(i)
            isNo = False
            break
