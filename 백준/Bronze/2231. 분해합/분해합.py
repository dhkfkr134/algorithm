# 근데 언제까지가 1000000까지인지 몰라서 대충 N의 생성자는 N보다 작으니깐 N까지로 돌림

N = int(input())
arr = [0]*1000001
isNo = True
a0 = 0
a1 = 0
a2 = 0
a3 = 0
a4 = 0
